#pragma once

#include <cmath>

// numerical constants
constexpr double pi = M_PIf64;       // pi = 4 atan(1.0)
constexpr double twopi = 2.0 * pi;   // 2 pi
constexpr double fourpi = 4.0 * pi;  // 4 pi

constexpr double pi_i = 1.0 / pi;          // 1 / pi
constexpr double twopi_i = 1.0 / twopi;    // 1 / (2 pi)
constexpr double fourpi_i = 1.0 / fourpi;  // 1 / (4 pi)

constexpr double sqrt2 = M_SQRT2f64;     // sqrt(2)
constexpr double sqrt2_i = 1.0 / sqrt2;  // 1 / sqrt(2)

constexpr double base_e = M_Ef64;
constexpr double ln2 = M_LN2f64;

constexpr double zero = 0.0;
constexpr double half = 0.5;
constexpr double one = 1.0;
constexpr double two = 2.0;
constexpr double three = 3.0;

constexpr double onethird = 1. / 3;
constexpr double twothird = 2 * onethird;
constexpr double fourthird = 4 * onethird;

